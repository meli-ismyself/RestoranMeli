public class ImageLocationItem {
    private String imgRestoran;
    private String namaRestoran;
    private String alamatRestoran;
    private String fasilitasRestoran;

    public String getImgRestoran() {
        return imgRestoran;
    }

    public void setImgRestoran(String imgRestoran) {
        this.imgRestoran = imgRestoran;
    }

    public String getNamaRestoran() {
        return namaRestoran;
    }

    public void setNamaRestoran(String namaRestoran) {
        this.namaRestoran = namaRestoran;
    }

    public String getAlamatRestoran() {
        return alamatRestoran;
    }

    public void setAlamatRestoran(String alamatRestoran) {
        this.alamatRestoran = alamatRestoran;
    }

    public String getFasilitasRestoran() {
        return fasilitasRestoran;
    }

    public void setFasilitasRestoran(String fasilitasRestoran) {
        this.fasilitasRestoran = fasilitasRestoran;
    }
}
